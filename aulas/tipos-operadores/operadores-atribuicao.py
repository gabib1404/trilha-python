saldo = 500
saldo += 200 # saldo = saldo + 200

print(saldo)

# Com subtração
saldo -= 100 # saldo = saldo - 100

print(saldo)

# Com multiplicação
saldo *= 2 # saldo = saldo * 2

print(saldo) 

# Com divisão
saldo /= 4 # saldo = saldo / 4

print(saldo)

# Com divisão inteira
saldo //= 100 # saldo = saldo // 100

print(saldo)

# Com módulo 
saldo %= 3 # saldo = saldo % 3

print(saldo)

# Com exponenciação
saldo **= 3 # saldo = saldo**3

print(saldo)

